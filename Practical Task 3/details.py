# Pseudocode:
# 1. Take user's name as input.
# 2. Take user's age as input.
# 3. Take user's house number as input.
# 4. Take user's street name as input.
# 5. Print out a single sentence containing all the details of the user.

# Step 1: Take user's name as input
name = input("Enter your name: ")

# Step 2: Take user's age as input
age = input("Enter your age: ")

# Step 3: Take user's house number as input
house_number = input("Enter your house number: ")

# Step 4: Take user's street name as input
street_name = input("Enter your street name: ")

# Step 5: Print out a single sentence containing all the details of the user
print(f"This is {name}. He is {age} years old and lives at house number {house_number} on {street_name}.")
 